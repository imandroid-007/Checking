package com.example.realestate.CCComponent;

public class Pojo_CC {

    private String id;
    private String cityName;
    private String label;
    private String ccIdColor;

    public Pojo_CC(String id, String cityName, String label, String ccIdColor) {
        this.id = id;
        this.cityName = cityName;
        this.label = label;
        this.ccIdColor = ccIdColor;
    }


    public String getId() {
        return id;
    }

    public String getCityName() {
        return cityName;
    }

    public String getLabel() {
        return label;
    }

    public String getCcIdColor() {
        return ccIdColor;
    }
}
















































































